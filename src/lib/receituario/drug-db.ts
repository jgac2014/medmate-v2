export function searchDrugs(query: string): Drug[] {
  const q = query.toLowerCase().trim();
  if (!q) return [];

  return DRUG_DB.filter((d) => {
    const haystack = `${d.name} ${d.form} ${d.manufacturer}`.toLowerCase();
    // Fuzzy: todos os tokens da query devem aparecer
    return q.split(" ").every((token) => haystack.includes(token));
  }).slice(0, 8);
}

export function getDrugById(id: string): Drug | undefined {
  return DRUG_DB.find((d) => d.id === id);
}
